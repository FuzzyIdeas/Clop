func migrateSettings() {
    guard let id = Bundle.main.bundleIdentifier else {
        return
    }

    let currentPrefs = URL.libraryDirectory
        .appendingPathComponent("Preferences")
        .appendingPathComponent(id == "com.lowtechguys.Clop-setapp" ? "com.lowtechguys.Clop-setapp.plist" : "com.lowtechguys.Clop.plist")
    let oldPrefs = URL.libraryDirectory
        .appendingPathComponent("Preferences")
        .appendingPathComponent(id == "com.lowtechguys.Clop-setapp" ? "com.lowtechguys.Clop.plist" : "com.lowtechguys.Clop-setapp.plist")

    if !FileManager.default.fileExists(atPath: currentPrefs.path), FileManager.default.fileExists(atPath: oldPrefs.path) {
        try? FileManager.default.copyItem(at: oldPrefs, to: currentPrefs)
        NSUbiquitousKeyValueStore.default.synchronize()
        restart()
    }
}

/// Seed the unified per-format `CompressionQuality` keys from the legacy aggressive/adaptive/encoder/bitrate
/// settings so existing users keep identical behaviour. Each version block runs exactly once; adding a new
/// block (for another format) never re-runs the earlier ones.
@MainActor func migrateToUnifiedCompression() {
    if Defaults[.compressionModelMigratedVersion] < 1 {
        // IMAGE: collapse the three per-format aggressive bools into one factor (any aggressive -> 64),
        // and map adaptiveImageSize onto the Adaptive tier.
        let anyAggressive = Defaults[.useAggressiveOptimisationJPEG]
            || Defaults[.useAggressiveOptimisationPNG]
            || Defaults[.useAggressiveOptimisationGIF]
        Defaults[.imageCompression] = CompressionQuality(
            tier: Defaults[.adaptiveImageSize] ? .adaptive : .custom,
            factor: anyAggressive ? COMPRESSION_FACTOR_AGGRESSIVE : COMPRESSION_FACTOR_NORMAL
        )
        Defaults[.compressionModelMigratedVersion] = 1
    }
    if Defaults[.compressionModelMigratedVersion] < 2 {
        // AUDIO: map the legacy absolute bitrate onto the factor for the current output format.
        // Negative sentinels (-1/-2 = "N steps lower than input") fall back to the default factor.
        let bitrate = Defaults[.audioBitrate]
        let factor = bitrate > 0 ? audioCompressionFactor(forBitrate: bitrate, format: Defaults[.audioFormat]) : 35
        Defaults[.audioCompression] = CompressionQuality(tier: .custom, factor: factor)
        Defaults[.compressionModelMigratedVersion] = 2
    }
    if Defaults[.compressionModelMigratedVersion] < 3 {
        // VIDEO: map the legacy encoder tier; useAggressiveOptimisationMP4 nudges the factor smaller.
        var cq = videoEncoderToCQ(Defaults[.videoEncoder])
        if Defaults[.useAggressiveOptimisationMP4], cq.tier != .lossless {
            cq = CompressionQuality(tier: .smaller, factor: COMPRESSION_FACTOR_AGGRESSIVE)
        }
        Defaults[.videoCompression] = cq
        Defaults[.compressionModelMigratedVersion] = 3
    }
    if Defaults[.compressionModelMigratedVersion] < 4 {
        /// Surface the new draggable compression button by default in place of the aggressive button.
        func withCompression(_ list: [FloatingAction]) -> [FloatingAction] {
            var l = list
            if let i = l.firstIndex(of: .aggressiveOptimisation) {
                l[i] = .compression
            } else if !l.contains(.compression) {
                l.insert(.compression, at: min(1, l.count))
            }
            var seen = Set<FloatingAction>()
            return l.filter { seen.insert($0).inserted }
        }
        Defaults[.floatingResultActions] = withCompression(Defaults[.floatingResultActions])
        Defaults[.compactResultActions] = withCompression(Defaults[.compactResultActions])
        Defaults[.compressionModelMigratedVersion] = 4
    }
    if Defaults[.compressionModelMigratedVersion] < 5 {
        // AUDIO per-format targets: map the old single output format + convert-set onto the new exclusive sets.
        // Old .mp3 output -> MP3 set; everything else (.aac, .opus, lossless, .sameAsInput) -> AAC set, so
        // lossless inputs still get compressed.
        let old = Defaults[.formatsToConvertToOutputAudio]
        if Defaults[.audioFormat] == .mp3 {
            Defaults[.formatsToConvertToMP3] = old
        } else {
            Defaults[.formatsToConvertToAAC] = old
        }
        Defaults[.compressionModelMigratedVersion] = 5
    }
}

