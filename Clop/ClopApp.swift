                        optimiser.stop(remove: true, animateRemoval: true)
                        self.swipeEnded = false
                    }
                    if event.scrollingDeltaX == 0 {
                        self.swipeEnded = true
                    }
                }
            }.store(in: &observers)

        NSEvent.addLocalMonitorForEvents(matching: .keyDown) { event in
            guard let hov = hoveredOptimiserID,
                  let optimiser = OM.optimisers.first(where: { $0.id == hov }),
                  event.modifierFlags.contains(.command)
            else { return event }

            switch event.charactersIgnoringModifiers {
            case "e":
                // Prefer the user's configured "Edit with" app for this file type; fall back to the
                // running shelf app (Yoink/Dropover/…) when none is set.
                if optimiser.editWithConfiguredApp() { return nil }
                guard let shelfApp = runningShelfApp() else { return event }
                shelfApp.open(optimiser: optimiser)
                return nil
            case "f":
                optimiser.showInFinder()
                return nil
            default:
                return event
            }
        }
    }

