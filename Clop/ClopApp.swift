    @MainActor
    func handleCommandHotkey(_ key: SauceKey) {
        guard finishedOnboarding, let opt = OM.hovered else {
            return
        }

        switch key {
        case .comma:
            WM.open("settings")
            focus()
        case .minus:
            // Step "down" maps to each type's own compression axis: DPI for PDF, bitrate for audio,
            // resolution for everything else. stepDownscale only drives image/video, so PDF/audio
            // need their dedicated steppers or the keypress just flashes the indicator.
            if opt.type.isPDF {
                opt.stepLowerPDFDPI()
            } else if opt.type.isAudio {
                opt.stepLowerBitrate()
            } else if opt.downscaleFactor > 0.1 {
                opt.stepDownscale()
            }
        case .x where opt.changePlaybackSpeedFactor < 10 && opt.canChangePlaybackSpeed():
            opt.changePlaybackSpeed()
        case .delete:
            hoveredOptimiserID = nil
            opt.stop(animateRemoval: true)
        case .z where !opt.isOriginal:
            opt.restoreOriginal()
            opt.overlayMessage = "Restored original"
            opt.collapseHoverOverlay = true
        case .r where !opt.running:
            opt.editingFilename = true
        case .d where opt.url != nil && opt.comparisonOriginalURL != nil:
            opt.compare()
        case .k where opt.canCrop() && !opt.running:
            opt.showCropWindow()
        case .c:
            opt.copyToClipboard()
            opt.overlayMessage = "Copied"
        case .s:
            opt.save()
        case .f:
            opt.showInFinder()
        case .u:
            DROPSHARE.open(optimiser: opt)
        case .w:
            if let session = WDM.session(forOptimiser: opt) {
                session.copyLink()
                opt.overlayMessage = "Copied link"
            } else {
                warpDropSend(optimiser: opt)
            }
        case .o:
            guard let url = opt.url ?? opt.originalURL else { return }
            NSWorkspace.shared.open(url)
        case .a where !opt.aggressive:
            if opt.downscaleFactor < 1 {
                opt.downscale(toFactor: opt.downscaleFactor, aggressiveOptimisation: true)
            } else {
                opt.optimise(allowLarger: false, aggressiveOptimisation: true, fromOriginal: true)
            }
        default:
            return
        }
    }

